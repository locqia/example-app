			<h1> Pengertian Framework</h1>
			<p>Pengertian Framework adalah sebuah kerangka kerja yang digunakan untuk mempermudah para developer software dalam membuat dan mengembangkan aplikasi atau software.
			<br> Framework sendiri berisikan fungsi dasar dan perintah yang lazim dipakai untuk membuat dan mengembangkan sebuah software atau aplikasi, dengan harapan aplikasi yang dibuat bisa dibangun secara lebih terstruktur, lebih cepat serta lebih tersusun dengan cukup rapi.

Framework juga diartikan sebagai komponen-komponen pemrograman yang sudah jadi dan siap digunakan kapan saja sehingga pengembang aplikasi tidak perlu membuat script yang sama untuk tugas yang sama.</br></p>
			</p>
			<h1> Fungsi Framework</h1>
			<br><p>Framework memiliki fungsi sebagai berikut ini:
			</br>
			<p><br>-Mempercepat proses pembuatan aplikasi, baik aplikasi berbasis desktop, mobile atau web.</br></p>
			<p>-Membantu para developer dalam perencanaan, pembuatan, dan pemeliharaan sebuah aplikasi.</p>
			<p><br>-Aplikasi yang dihasilkan menjadi lebih stabil dan handal karena framework sudah melalui proses uji, baik uji stabilitas dan kehandalannya.</br></p>
			<p><br>-Memudahkan para developer dalam membaca kode program dan lebih mudah dalam mencari bugs.</br></p>
			<p><br>-Memiliki tingkat keamanan yang lebih karena framework telah mengantisipasi cela-cela keamanan yang mungkin timbul.</br></p>
			<p><br>-Memudahkan developer dalam mendokumentasikan aplikasi-aplikasi yang sedang dibangun.</br></p>
