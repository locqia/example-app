			<h1> Jenis Framework</h1>
			<p><br>CodeIgniter:</br></p>
			<p>CodeIgniter merupakan salah satu Framework yang dikembangkan oleh Rick Ellis berdasarkan script PHP. Selanjutnya CodeIgniter disempurnakan lagi oleh British Columbia Institute of Technology. Saat ini CodeIgniter sudah dikembangkan hingga versi yang ke-4.</p>
			<p><br>Laravel:</br></p>
			<p>Laravel merupakan salah satu web framework yang cukup terkenal dibandingkan yang lainnya. Framework yang satu ini diciptakan oleh Taylor Otwell yang hingga kini terus bekerja keras untuk dapat membangun Laravel menjadi mudah dan nyaman digunakan. Laravel pun memiliki dokumentasi yang lengkap dan jelas untuk memudahkan para web developer dalam membangun aplikasi web mereka.</p>
			<p><br>Yii:</br></p>
			<p>Web Framework satu ini mempunyai slogan “The Fast, Secure, and Professional PHP Framework.“ Yii juga memiliki dokumentasi dan dukungan komunitas yang kuat. Framework yang satu ini juga memiliki fitur-fitur unggulan, seperti sistem widget, filter, dan RBAC. Keunggulan-keunggulan tersebut  tentu saja membuat Yii menjadi salah satu web framework yang perlu diperhitungkan.</p>
			<p><br>Symfony:</p>
			<p>Symfony memiliki panduan yang cukup lengkap, dari cookbook hingga e-book yang mana semua itu hanya untuk memudahkan para penggunanya. Selain itu, Symfony juga memiliki dokumentasi yang rapi dan jelas. Bahkan memiliki teknologi yang cukup lengkap dibandingkan dengan web framerowk PHP lainnya.</br></p>
			<p>Tidak hanya itu saja, salah satu web framework terkenal, yakni Laravel, kabarnya pun turut serta menggunakan beberapa komponen yang terdapat pada Symfony. CMS terkenal seperti Magento dan Drupal pun dikabarkan dibuat dengan menggunakan teknologi Symfony.</p>
